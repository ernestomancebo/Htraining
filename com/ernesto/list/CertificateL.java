package com.ernesto.list;

public class CertificateL {

    private int id;
    private String name;

    public CertificateL() {

    }

    public CertificateL(String name) {
        this.name = name;
    }

    public int getId() {
        return id;
    }

    public String getName() {
        return name;
    }

    public void setId(int id) {
        this.id = id;
    }
    public void setName(String name) {
        this.name = name;
    }
}